/* 
============================================================================
Stored Procedure: Load Bronze Layer (Source -> Bronze)
============================================================================
Script Purpose:
     This stroed procedure loads data into the 'bronze' schema from external CSV files.
     It perform the following actions:
     - Truncates the bronze tables before loading data.
     - Uses the 'BULK INSERT' command to load data from CSV files to bronze tables.

Parameters:
     None
  This stored procedure does not accept any parameteres or return any values.

Usage Examples:
  Exec bronze.load_bronze;
======================================================================================
*/




create or alter procedure bronze.load_bronze as 
begin
    Declare @start_time datetime, @end_time datetime;
    BEGIN TRY
    print '========================================================';
    print 'Loading Bronze Layer';
    print '========================================================';

    Print '--------------------------------------------------------';
    print 'Loading CRM Tables';
    print '--------------------------------------------------------';



    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.crm_cust_info';
    truncate table bronze.crm_cust_info;
   
    print '>> Inserting Data Into:bronze.crm_cust_info';
    bulk insert bronze.crm_cust_info
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_crm\cust_info.csv'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );

    set @end_time = GETDATE();
    print '>> Load Duration:' +cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';
     print '--------------------------------------------------------'




    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.crm_prd_info';

    truncate table bronze.crm_prd_info;

    print '>> Inserting Data Into:bronze.crm_prd_info';

    
    bulk insert bronze.crm_prd_info
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_crm\prd_info.csv'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );
    set @end_time = GETDATE();
    print '>> Load Duration:' +cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';

    print '--------------------------------------------------------'



    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.crm_sales_details'; 
    truncate table bronze.crm_sales_details;
    
    print '>> Inserting Data into:bronze.sales_details';
    bulk insert bronze.crm_sales_details
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_crm\sales_details.csv'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );
    set @end_time = GETDATE();
    print '>> Load Duration:'+ cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';

    print '--------------------------------------------------------'



    Print '--------------------------------------------------------';
    print 'Loading ERP Tables';
    print '--------------------------------------------------------'


    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.erp_loc_a101';
    truncate table bronze.erp_loc_a101;
   
    print '>> Inserting Data into:bronze.erp_loc_a101';
    bulk insert bronze.erp_loc_a101
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_erp\LOC_A101.CSV'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );
    set @end_time = GETDATE();
    print '>> Load Duration:' +cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';

    print '--------------------------------------------------------'

    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.erp_cust_az12';
    truncate table bronze.erp_cust_az12;
    
    print '>> Inserting Data Into:bronze.erp_cust_az12';
    bulk insert bronze.erp_cust_az12
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_erp\CUST_az12.csv'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );
    set @end_time = GETDATE();
    print '>> Load Duration:' +cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';



    Set @start_time = GETDATE();
    print '>> Truncating Tables:bronze.erp_cat_g1v2';
    truncate table bronze.erp_cat_g1v2;
    
    print '>> Inserting Data Into:bronze.erp_cat_g1v2';
    bulk insert bronze.erp_cat_g1v2
    from 'C:\Project\Datawarehouse\sql-data-warehouse-project\datasets\source_erp\px_cat_g1v2.csv'
    with (
        firstrow = 2,
        Fieldterminator = ',',
        tablock
    );
    set @end_time = GETDATE();
    print '>> Load Duration:' +cast(datediff(second,@start_time,@end_time) as nvarchar) + 'seconds';

    print '--------------------------------------------------------'
end try
begin catch
print '======================================================'
print ' Error occured during loading bronze layer'
print ' Error message' + Error_message();
print ' Error message' + cast(error_number() as nvarchar);
print ' Error message' + cast(error_state() as nvarchar);
print '======================================================'
end catch
end;
